export function progLink(name : string) {
    switch(name) {
        case "Android" : return "https://developer.android.com/"
        case "Chakra" : return "https://v2.chakra-ui.com/"
        case "Kotlin" : return "https://kotlinlang.org/"
        case "Mongo DB" : return "https://www.mongodb.com/"
        case "React" : return "https://react.dev/"
        case "Spring Boot" : return "https://spring.io/projects/spring-boot"
        case "TypeScript" : return "https://www.typescriptlang.org/"
        case "Java" : return "https://www.java.com/en/"
        
    }
    return "Link not found"
}